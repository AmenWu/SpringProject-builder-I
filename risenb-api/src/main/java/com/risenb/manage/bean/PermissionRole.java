package com.risenb.manage.bean;

import java.io.Serializable;
import java.util.Date;

/*
 * 权限角色关联实体
 * */
public class PermissionRole implements Serializable {

	private static final long serialVersionUID = 694011725970399456L;

	private Integer relId;   //权限角色关联ID
	private Integer roleId;  //角色ID
	private Integer permissionId;  //权限ID
	private Integer manageId;   //管理员ID
	private Date operaTime;     //操作时间
	private String title;//临时字段  权限名称
	
	public Integer getRelId() {
		return relId;
	}
	public void setRelId(Integer relId) {
		this.relId = relId;
	}
	public Integer getRoleId() {
		return roleId;
	}
	public void setRoleId(Integer roleId) {
		this.roleId = roleId;
	}
	public Integer getPermissionId() {
		return permissionId;
	}
	public void setPermissionId(Integer permissionId) {
		this.permissionId = permissionId;
	}
	
	/**
	 * @return the manageId
	 */
	public Integer getManageId() {
		return manageId;
	}
	/**
	 * @param manageId the manageId to set
	 */
	public void setManageId(Integer manageId) {
		this.manageId = manageId;
	}
	public Date getOperaTime() {
		return operaTime;
	}
	public void setOperaTime(Date operaTime) {
		this.operaTime = operaTime;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	
	
}
